package com.tiancheng.mobilefront.bean;

public class InfoS {
	private String ID_;
	private String NAME_;
	private String SFZ_;
	private String YB_;
	private String ADDRES_;
	private String EMAIL_;
	private String PHONENUM;

	public String getID_() {
		return ID_;
	}

	public void setID_(String iD_) {
		ID_ = iD_;
	}

	public String getNAME_() {
		return NAME_;
	}

	public void setNAME_(String nAME_) {
		NAME_ = nAME_;
	}

	public String getSFZ_() {
		return SFZ_;
	}

	public void setSFZ_(String sFZ_) {
		SFZ_ = sFZ_;
	}

	public String getYB_() {
		return YB_;
	}

	public void setYB_(String yB_) {
		YB_ = yB_;
	}

	public String getADDRES_() {
		return ADDRES_;
	}

	public void setADDRES_(String aDDRES_) {
		ADDRES_ = aDDRES_;
	}

	public String getEMAIL_() {
		return EMAIL_;
	}

	public void setEMAIL_(String eMAIL_) {
		EMAIL_ = eMAIL_;
	}

	public String getPHONENUM() {
		return PHONENUM;
	}

	public void setPHONENUM(String pHONENUM) {
		PHONENUM = pHONENUM;
	}

}
